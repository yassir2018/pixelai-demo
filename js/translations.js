let currentLang = localStorage.getItem('pixelai_lang') || 'ar';

function t(key) {
  return (translations[currentLang] && translations[currentLang][key]) || translations['en'][key] || key;
}

function setLanguage(lang) {
  currentLang = lang;
  localStorage.setItem('pixelai_lang', lang);
  document.documentElement.setAttribute('dir', translations[lang].dir);
  document.documentElement.setAttribute('lang', lang);
  document.documentElement.style.fontFamily = translations[lang].font + ', sans-serif';
  applyTranslations();
  updateLangLabel();
  // Re-apply admin content after translations (so admin text wins over i18n defaults)
  if (window.__applyPixelContent) window.__applyPixelContent();
}

function updateLangLabel() {
  const labels = { ar: 'عربي', en: 'EN', fr: 'FR' };
  const el = document.getElementById('currentLangLabel');
  if (el) el.textContent = labels[currentLang] || 'EN';
}

function applyTranslations() {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    const attr = el.getAttribute('data-i18n-attr');
    const val = t(key);
    if (attr) {
      el.setAttribute(attr, val);
    } else {
      el.textContent = val;
    }
  });
  document.querySelectorAll('[data-i18n-html]').forEach(el => {
    el.innerHTML = t(el.getAttribute('data-i18n-html')).replace(/\n/g, '<br>');
  });
}
